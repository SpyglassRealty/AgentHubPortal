function normalize(s: string): string {
  return s
    .toLowerCase()
    .replace(/[\s\-\.\']+/g, ' ')
    .trim();
}

interface MatchResult {
  sourceName: string;
  county: string;
  slug: string | null;
  dbName: string | null;
  type: 'exact' | 'fuzzy' | 'missing';
  fuzzyCandidate?: { slug: string; dbName: string };
}

async function processCounty(
  names: string[],
  county: string,
): Promise<MatchResult[]> {
  // Pull all communities rows for this county (case-insensitive)
  const rows = await db
    .select({ slug: communities.slug, name: communities.name })
    .from(communities)
    .where(sql`LOWER(${communities.county}) = LOWER(${county})`);

  const byLowerName = new Map<string, { slug: string; name: string }>();
  const byNormName = new Map<string, { slug: string; name: string }>();
  for (const row of rows) {
    byLowerName.set(row.name.toLowerCase(), row);
    byNormName.set(normalize(row.name), row);
  }

  const results: MatchResult[] = [];

  for (const sourceName of names) {
    const lowerSource = sourceName.toLowerCase();
    const normSource = normalize(sourceName);

    const exactRow = byLowerName.get(lowerSource);
    if (exactRow) {
      results.push({ sourceName, county, slug: exactRow.slug, dbName: exactRow.name, type: 'exact' });
      continue;
    }

    // Fuzzy fallback
    const fuzzyRow = byNormName.get(normSource);
    if (fuzzyRow) {
      results.push({
        sourceName,
        county,
        slug: null,
        dbName: null,
        type: 'fuzzy',
        fuzzyCandidate: { slug: fuzzyRow.slug, dbName: fuzzyRow.name },
      });
      continue;
    }

    results.push({ sourceName, county, slug: null, dbName: null, type: 'missing' });
  }

  return results;
}

async function getExistingCounts(): Promise<Record<string, number>> {
  const rows = await db
    .select({ county: pulseCommunityAllowlist.county, cnt: count() })
    .from(pulseCommunityAllowlist)
    .groupBy(pulseCommunityAllowlist.county);
  const out: Record<string, number> = {};
  for (const r of rows) out[r.county] = Number(r.cnt);
  return out;
}

