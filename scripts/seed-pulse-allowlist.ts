async function main() {
  console.log(`\n=== seed-pulse-allowlist ${DRY_RUN ? '[DRY RUN]' : '[LIVE]'} ===\n`);

  const countyMap: Array<{ county: string; names: string[] }> = [
    { county: 'Travis', names: TRAVIS_NAMES },
    { county: 'Williamson', names: WILLIAMSON_NAMES },
    { county: 'Hays', names: HAYS_NAMES },
  ];

  const allResults: MatchResult[] = [];

  for (const { county, names } of countyMap) {
    const results = await processCounty(names, county);
    allResults.push(...results);
  }

  const exact = allResults.filter(r => r.type === 'exact');
  const fuzzy = allResults.filter(r => r.type === 'fuzzy');
  const missing = allResults.filter(r => r.type === 'missing');

  // Insert exact matches
  if (!DRY_RUN && exact.length > 0) {
    const rows = exact.map(r => ({
      slug: r.slug!,
      county: r.county,
    }));
    await db
      .insert(pulseCommunityAllowlist)
      .values(rows)
      .onConflictDoNothing();
  }

  // ---------------------------------------------------------------------------
  // Report
  // ---------------------------------------------------------------------------

  if (fuzzy.length > 0) {
    console.log('--- FUZZY MATCH CANDIDATES (not inserted — review required) ---');
    for (const r of fuzzy) {
      console.log(
        `  [${r.county}] "${r.sourceName}"  →  db: "${r.fuzzyCandidate!.dbName}" (slug: ${r.fuzzyCandidate!.slug})`,
      );
    }
    console.log();
  }

  if (missing.length > 0) {
    console.log('--- MISSING (no match found) ---');
    for (const r of missing) {
      console.log(`  [${r.county}] "${r.sourceName}"`);
    }
    console.log();
  }

  const existingCounts = await getExistingCounts();
  const totalExisting = Object.values(existingCounts).reduce((a, b) => a + b, 0);

  console.log('--- SUMMARY ---');
  console.log(`Exact matches: ${exact.length} (${DRY_RUN ? 'would insert' : 'inserted'})`);
  console.log(`Fuzzy match candidates: ${fuzzy.length} (listed above, not inserted)`);
  console.log(`Missing: ${missing.length} (listed above)`);
  console.log();
  console.log(`Existing allowlist rows after run: ${totalExisting}`);
  for (const [county, cnt] of Object.entries(existingCounts)) {
    console.log(`  ${county}: ${cnt}`);
  }
  console.log();
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
